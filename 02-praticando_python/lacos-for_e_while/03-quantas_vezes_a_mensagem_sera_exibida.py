# Ajude Marcos a escrever um programa que exiba a mensagem: "Bem-vindo ao Buscante!" o número exato de vezes que ele precisa.

mensagem = 'Bem-vindo ao Buscante!'
quantidade = 5 # Defina aqui o número de vezes que a mensagem deve ser exibida

# range() cria uma sequência de números de 0 até a quantidade definida
for _ in range(quantidade):
    print(mensagem)
